
public class Principal {

	public static void main(String[] args) {

		int numero = 10; //  Declaramos la variable "numero" con un valor de 10. 
		
		numero = 20; // Cambio el valor de la variable "numero"
		
		System.out.println("La variable es " +numero);
		
		// La variable es, valga la redundancia, variable; pero podemos definir constantes cuyo valor permanecerá
		// constante - valga la redundancia - hasta el final de la ejecución del programa. 
		
		final int numero_constante = 10; // con final int declaramos una constante.
		
		System.out.println("La constante es " +numero_constante);
		
		// si queremos cambiar el valor de la constante (numero_constante = 20; ), el programa no nos dejará, 
		
				
			

	}

}
