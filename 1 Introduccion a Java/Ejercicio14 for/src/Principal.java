
public class Principal {

	public static void main(String[] args) {
			
		
		
		// for (inicializacion ; condicion ; funcion) 
		
		// considerando que entero =1,  (mientras 10 es mayor o igual que entero), entero suma 1
		
		for (int entero =1; entero<=10; entero++) {
			
			System.out.println (entero); // El ciclo se repite. 
		}
		
	
		 
		// para la variable entero = 1 ; si es menor o igual que 10 ; sumar 1 
		
	}

}
